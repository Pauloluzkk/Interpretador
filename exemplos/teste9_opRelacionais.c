#include <stdio.h>

int main(void) {
    int x = 10;
    int y = 20;

    if (x < y) {
        printf("x < y: verdadeiro\n");
    }

    if (x <= 10) {
        printf("x <= 10: verdadeiro\n");
    }

    if (x == 10) {
        printf("x == 10: verdadeiro\n");
    }

    if (x != y) {
        printf("x != y: verdadeiro\n");
    }

    return 0;
}